           <u>Adding products to Store</u>
           <br/>
           <br/>
            If you want to add new products, go to the 'Add Products' on the navigation bar on the left, a form will be displayed, enter the required details and click 'Add to my Product List'. 
            <br/>
            To check the items in your Store you can click on 'Products' button in the navigation bar.
            <br/>
            <br/>
            <u>Updating details of products</u>
            <br/>
            <br/>
            If you want to change or update the details of a product. 
            <br/>
            Click on the 'Update Products' button in the navigation bar. Use the product name as key and enter the values to be changed and Click 'Update'.
            <br/>
            <br/>
            <u>Remove A Product From Store</u>
            <br/>
            <br>
            To remove a product from store use the 'Update Product' option and change its quantity to 0.
            <br/>
            The products with quantity 0 are not displayed to the customers.
            <br/>
            <br/>
            <u>Logging out of the portal</u>
            <br/>
            <br>
